    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String codigo = txtCodigo.getText().toString();
        String horario = txtHorario.getText().toString();
        String requisitos = txtRequisitos.getText().toString();
        String planestudio = txtPlanEstudio.getText().toString();
        int duracion = Integer.parseInt(txtDuracion.getText().toString());

        //enviar
        asignatura.setCodigo(codigo);
        asignatura.setDuracion(duracion);
        asignatura.setHorario(horario);
        asignatura.setPlanestudio(planestudio);
        asignatura.setRequisitos(requisitos);
        
        //mensaje de verificaicon correcta
        JOptionPane.showMessageDialog(rootPane, "se registro correctamente");
        
        //limpiar la caja de texto
        txtCodigo.setText("");
        txtHorario.setText("");
        txtRequisitos.setText("");
        txtPlanEstudio.setText("");
        txtDuracion.setText("");
        txtCodigo.setText("");
    }//GEN-LAST:event_btnRegistrarActionPerformed

