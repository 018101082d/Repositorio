    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String codigo = txtCodigo.getText().toString();
        String apellidos = txtApellido.getText().toString();
        String nombres = txtNombres.getText().toString();
        String correo = txtCorreElectronico.getText().toString();
        String seguro = txtSegurodeVida.getText().toString();

        //enviar
        alumno.setCodigo(codigo);
        alumno.setApellido(apellidos);
        alumno.setNombres(nombres);
        alumno.setCorreoelectronico(correo);
        alumno.setSegurovida(seguro);
        
        //mensaje de verificaicon correcta
        JOptionPane.showMessageDialog(rootPane, "se registro correctamente");
        
        //limpiar la caja de texto
        txtCodigo.setText("");
        txtApellido.setText("");
        txtNombres.setText("");
        txtCorreElectronico.setText("");
        txtSegurodeVida.setText("");        
    }//GEN-LAST:event_btnRegistrarActionPerformed

