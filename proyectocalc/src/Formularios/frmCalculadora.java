    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        Operaciones operaciones= new Operaciones(nro,Double.parseDouble(txtBox.getText()));
        switch(operacion){
            case "+":
            txtBox.setText(String.valueOf(operaciones.Sumar()));
            break;
            case "-":
            txtBox.setText(String.valueOf(operaciones.Restar()));
            break;
            case "/":
            txtBox.setText(String.valueOf(operaciones.Dividir()));
            break;
            case "*":
            txtBox.setText(String.valueOf(operaciones.Multiplicar()));
            break;
            case "%":
            txtBox.setText(String.valueOf(operaciones.Porcentaje()));
            break;
            
        }
        
    }//GEN-LAST:event_btnIgualActionPerformed

